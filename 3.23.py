#Exercise 3.23:
#Writing for loop using a range:

for i in range(2):
    print(i,end=" ")
print("\n")
for i in range(1):
    print(i,end=" ")
print("\n")
for i in range(3,7):
    print(i,end=" ")
print("\n")
for i in [x for x in range(2) if x!=0]:
    print(i,end=" ")
print("\n")
for i in range(0,4,3):
    print(i,end=" ")
print("\n")
for i in range(5,22,4):
    print(i,end=" ")
print("\n")
